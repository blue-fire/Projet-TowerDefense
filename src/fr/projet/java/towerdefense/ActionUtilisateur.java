package fr.projet.java.towerdefense;

/**
 * @author Romain Les action que l'utilisateurs peut utiliser.
 */
public enum ActionUtilisateur {

	/**
	 * Supprimer une tour.
	 */
	supprimerTour(),
	/**
	 * Creer une tour
	 */
	creerTour(),
	/**
	 * Ameliorer une tour.
	 */
	ameliorerTour(),
	/**
	 * Finir le tour de jeu.
	 */
	finirLeTour(),
	/**
	 * Annuler l'action.
	 */
	annuler;
}
